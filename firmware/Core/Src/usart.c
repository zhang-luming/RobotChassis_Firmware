/* ==================== DMA发送辅助函数 ==================== */

/**
 * @brief 检查DMA发送是否忙碌
 * @return 1=忙碌，0=空闲
 */
uint8_t USART2_DMA_IsBusy(void) {
  return dma_tx_busy;
}

/**
 * @brief 设置DMA发送忙碌标志
 * @param busy 1=忙碌，0=空闲
 * @note 在启动DMA传输前调用，设置为1
 */
void USART2_DMA_SetBusy(uint8_t busy) {
  dma_tx_busy = busy;
}

/**
 * @brief 等待DMA发送完成（带超时）
 * @param timeout_ms 超时时间（毫秒）
 * @return HAL_OK=成功，HAL_TIMEOUT=超时
 */
HAL_StatusTypeDef USART2_DMA_WaitForComplete(uint32_t timeout_ms) {
  uint32_t start = HAL_GetTick();
  while (dma_tx_busy) {
    if ((HAL_GetTick() - start) >= timeout_ms) {
      return HAL_TIMEOUT;
    }
  }
  return HAL_OK;
}

/**
 * @brief DMA发送完成回调
 * @param huart UART句柄
 */
void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart) {
  if (huart->Instance == USART2) {
    dma_tx_busy = 0;
  }
}

